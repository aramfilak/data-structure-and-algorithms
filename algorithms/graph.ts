// Basics of graph theory
// 1. Technical terms
//    - Vertex: A node in a graph.
//    - Edge: A connection between two vertices.
//    - Adjacent: Two vertices are adjacent if they are connected by an edge.
//    - Loop: An edge that connects a vertex to itself.
//    - Path: A path is a sequence of vertices where each adjacent pair is connected by an edge.
//    - Cycle: A path that starts and ends at the same vertex.
// 2. Types of graphs
//    - Directed graph: A graph where the edges have a direction.
//    - Undirected graph: A graph where the edges do not have a direction.
//    - Weighted graph: A graph where the edges have a weight.
//    - Unweighted graph: A graph where the edges do not have a weight.
// 3. Graph representations
//    - Adjacency matrix: A 2D array where the value at matrix[i][j] represents the edge between vertices i and j.
//    - Adjacency list: A list where the index represents a vertex and the value represents the adjacent vertices.
// 4. Graph traversal
//    - Depth-first search (DFS): A graph traversal algorithm that explores as far as possible along each branch before backtracking.
//    - Breadth-first search (BFS): A graph traversal algorithm that explores all the vertices at the present depth before moving on to the vertices at the next depth.
